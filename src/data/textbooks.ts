// Ethiopian MoE student textbook catalogue.
// PDFs are linked directly from the Ministry of Education's e-Learn portal:
//   https://elearn.moe.gov.et  (FDRE Ministry of Education — Student Textbooks)
//
// URL pattern observed on elearn.moe.gov.et:
//   https://elearn.moe.gov.et/storage/{subject}/text%20book/{Filename}.pdf
//
// We link to the canonical book page on the portal so users always get the
// latest official edition even if filenames change.

